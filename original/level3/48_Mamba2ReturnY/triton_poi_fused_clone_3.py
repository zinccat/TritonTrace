@triton.jit
def triton_poi_fused_clone_3(in_ptr0, in_ptr1, out_ptr0, out_ptr1, ynumel, xnumel, YBLOCK : tl.constexpr, XBLOCK : tl.constexpr):
    ynumel = 4096
    xnumel = 64
    yoffset = tl.program_id(1) * YBLOCK
    yindex = yoffset + tl.arange(0, YBLOCK)[None, :]
    ymask = tl.full([XBLOCK, YBLOCK], True, tl.int1)
    xoffset = tl.program_id(0) * XBLOCK
    xindex = xoffset + tl.arange(0, XBLOCK)[:, None]
    xmask = xindex < xnumel
    x2 = xindex
    y0 = (yindex % 128)
    y1 = yindex // 128
    y7 = yindex
    y4 = ((yindex // 16) % 8)
    y5 = ((yindex // 128) % 2)
    y6 = yindex // 256
    tmp0 = tl.load(in_ptr0 + (y0 + 128*x2 + 8192*y1), xmask, eviction_policy='evict_last')
    tmp1 = tl.load(in_ptr1 + (63 + 64*y5 + 128*y4 + 1024*y6), None, eviction_policy='evict_last')
    tmp2 = tl.load(in_ptr1 + (x2 + 64*y5 + 128*y4 + 1024*y6), xmask, eviction_policy='evict_last')
    tmp3 = tmp1 - tmp2
    tmp4 = tl_math.exp(tmp3)
    tmp5 = tmp0 * tmp4
    tl.store(out_ptr0 + (x2 + 64*y7), tmp0, xmask)
    tl.store(out_ptr1 + (x2 + 64*y7), tmp5, xmask)
